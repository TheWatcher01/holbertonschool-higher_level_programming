#!/usr/bin/python3

"""
def common_elements(set_1, set_2):

Description:

'common_elements' function takes two sets as arguments.
It returns a new set with the common elements of both sets.
This is achieved by using the intersection operator '&' on the two sets.

Arguments:
"set_1" - first set of elements
"set_2" - second set of elements

Return:
A set with common elements of 'set_1' and 'set_2'.
"""


def common_elements(set_1, set_2):
    # Define 'common_elements' with two parameters: 'set_1' and 'set_2'.
    # Use intersection operator '&' to find common elements...
    # ... of 'set_1' and 'set_2'.

    return set_1 & set_2
    # Return the set with common elements of 'set_1' and 'set_2'.
